package composicao;

public class Elfo {
	
	private String nome;
	private int level;
	private String arma;
	
	public Elfo(String nome, int level, String arma) {
		super();
		this.nome = nome;
		this.level = level;
		this.arma = arma;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getArma() {
		return arma;
	}

	public void setArma(String arma) {
		this.arma = arma;
	}
	
	
	
	

}
